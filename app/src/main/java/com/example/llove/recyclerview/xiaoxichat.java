package com.example.llove.recyclerview;

public class xiaoxichat {
    private int touxiang;
    private String dianming;
    private String endxiaoxi;

    public xiaoxichat(int touxiang, String dianming, String endxiaoxi) {
        this.touxiang = touxiang;
        this.dianming = dianming;
        this.endxiaoxi = endxiaoxi;
    }

    public int getTouxiang() {
        return touxiang;
    }

    public void setTouxiang(int touxiang) {
        this.touxiang = touxiang;
    }

    public String getDianming() {
        return dianming;
    }

    public void setDianming(String dianming) {
        this.dianming = dianming;
    }

    public String getEndxiaoxi() {
        return endxiaoxi;
    }

    public void setEndxiaoxi(String endxiaoxi) {
        this.endxiaoxi = endxiaoxi;
    }
}
